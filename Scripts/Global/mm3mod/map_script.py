def char_to_tile(c):
    if c in ',^V':
        return 1

    if c in 'Ww~':
        return 133
    
    if c in 'T123456?': # special
        return 173
    
    if c in 'sSdpGT123456': # snow
        return 173

    if c == 'r': # road
        return 198
    
    if c in ' .g': # grass
        return 97
    if c == ';': # swamp
        return 97
    
    if c == 'f': # light forest
        return 98
    if c == 'F': # thick forest
        return 99
    
    return 97

RND_SIZE = 45
rmap=[[random.randint(0,10) for _ in range(RND_SIZE)] for _ in range(RND_SIZE)]
def char_to_z(c,tx=0,ty=0):
    if c in '^GV':
        return 50 + 2*rmap[tx%RND_SIZE][ty%RND_SIZE]

    if c == 'F':
        return 30+rmap[tx%RND_SIZE][ty%RND_SIZE]

    if c == 'f':
        return 5

    if c == '"':
        return 5

    if c == 'e':
        return rmap[tx%RND_SIZE][ty%RND_SIZE]

    if c in '. ;':
        return 0

    if c in 'wW~':
        return 0
    return 0


