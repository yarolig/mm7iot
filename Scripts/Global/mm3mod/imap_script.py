# d - make all sides doors, except d# and d, and dD
# s - make all sides weak walls, except s# and s,
# D - same as d but solid
def main():
    s=''
    for i in range(17):
        for ii in range(2):
            for j in range(17):
                for jj in range(2):
                    if ii in [0,2] and jj in [0,2]:
                        s+='#'
                    elif ii==1 and jj == 1:
                        s+='.'
                    elif ii in [0,2]:
                        s+='-'
                    elif jj in [0,2]:
                        s+='|'
                    else:
                        s+='?'
            s+='\n'
    print(s)
                    
main()